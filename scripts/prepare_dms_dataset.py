"""
下载 4 类 DMS 检测数据集（来源：Open Images V7，Google 公开数据集）

类别映射：
  0  face       ← Human face
  1  phone      ← Mobile phone
  2  cigarette  ← Cigarette
  3  seatbelt   ← Seat belt

输出目录：datasets/dms4class/
  train/images/ + train/labels/
  val/images/   + val/labels/
  data.yaml

用法：
  python scripts/prepare_dms_dataset.py
  python scripts/prepare_dms_dataset.py --n-train 800 --n-val 150
"""
from __future__ import annotations

import argparse
import shutil
from pathlib import Path

from PIL import Image

ROOT    = Path(__file__).resolve().parents[1]
OUT_DIR = ROOT / "datasets" / "dms4class"

# Open Images 类别名 → 我们的 YOLO 类别 ID
CLASS_MAP: dict[str, int] = {
    "Human face":   0,
    "Mobile phone": 1,
    "Cigarette":    2,
    "Seat belt":    3,
}
ID_TO_NAME = {v: k.split()[-1].lower() for k, v in CLASS_MAP.items()}
# 0→face, 1→phone, 2→cigarette, 3→belt  (用于打印)


def export_samples(dataset, out_dir: Path) -> tuple[int, int]:
    """把 fiftyone dataset 导出为 YOLO txt 格式，返回 (图片数, 标注框数)."""
    img_dir = out_dir / "images"
    lbl_dir = out_dir / "labels"
    img_dir.mkdir(parents=True, exist_ok=True)
    lbl_dir.mkdir(parents=True, exist_ok=True)

    n_img, n_box = 0, 0
    seen: set[str] = set()

    for sample in dataset.iter_samples(progress=True):
        src = Path(sample.filepath)
        key = src.name
        if key in seen:          # 同一张图被多次下载（不同类别请求重叠）
            continue
        seen.add(key)

        # 获取图片尺寸
        try:
            meta = sample.metadata
            w = meta.width if meta else None
            h = meta.height if meta else None
        except Exception:
            w = h = None
        if not w or not h:
            with Image.open(src) as im:
                w, h = im.size

        # 构建 YOLO 标注行
        lines: list[str] = []
        dets = sample.ground_truth
        if dets:
            for det in dets.detections:
                cls_id = CLASS_MAP.get(det.label)
                if cls_id is None:
                    continue
                bx, by, bw, bh = det.bounding_box   # 归一化，左上角
                cx = bx + bw / 2
                cy = by + bh / 2
                lines.append(f"{cls_id} {cx:.6f} {cy:.6f} {bw:.6f} {bh:.6f}")

        if not lines:
            continue   # 跳过没有目标类别标注的图

        shutil.copy2(src, img_dir / key)
        (lbl_dir / (src.stem + ".txt")).write_text("\n".join(lines))
        n_img += 1
        n_box += len(lines)

    return n_img, n_box


def download_class(cls_name: str, split: str, n: int) -> "fo.Dataset":
    import fiftyone.zoo as foz
    ds_name = f"dms_{split}_{cls_name.replace(' ', '_')}"
    return foz.load_zoo_dataset(
        "open-images-v7",
        split=split,
        label_types=["detections"],
        classes=[cls_name],
        max_samples=n,
        dataset_name=ds_name,
        overwrite=True,
    )


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--n-train", type=int, default=500,
                        help="每个类别的训练图片数（默认 500）")
    parser.add_argument("--n-val",   type=int, default=100,
                        help="每个类别的验证图片数（默认 100）")
    args = parser.parse_args()

    try:
        import fiftyone as fo
    except ImportError:
        print("请先安装 fiftyone：")
        print("  pip install fiftyone")
        return

    print("=" * 60)
    print("DMS 4 类数据集准备工具")
    print(f"  来源  : Open Images V7（Google 公开数据集）")
    print(f"  类别  : face / phone / cigarette / seatbelt")
    print(f"  训练集: {args.n_train} 张/类  共 {args.n_train * 4} 张")
    print(f"  验证集: {args.n_val}  张/类  共 {args.n_val  * 4} 张")
    print(f"  输出  : {OUT_DIR}")
    print("=" * 60)

    for split, n in [("train", args.n_train), ("validation", args.n_val)]:
        out_split = OUT_DIR / ("train" if split == "train" else "val")
        print(f"\n[{split}] 开始下载...")
        total_img = total_box = 0

        for cls_name in CLASS_MAP:
            print(f"  ↓ {cls_name}  (最多 {n} 张)")
            ds = download_class(cls_name, split, n)
            ni, nb = export_samples(ds, out_split)
            print(f"    → 导出 {ni} 张图 / {nb} 个标注框")
            total_img += ni
            total_box += nb
            fo.delete_dataset(ds.name)   # 清理 fiftyone 内部缓存

        print(f"  [{split}] 合计: {total_img} 张图 / {total_box} 个标注框")

    # 写 data.yaml
    yaml_text = f"""\
# DMS 4-class detection dataset
# Generated by scripts/prepare_dms_dataset.py

path: {OUT_DIR.as_posix()}
train: train/images
val:   val/images

nc: 4
names: [face, phone, cigarette, seatbelt]
"""
    yaml_path = OUT_DIR / "data.yaml"
    yaml_path.write_text(yaml_text)

    print("\n" + "=" * 60)
    print("完成！")
    print(f"  数据集路径 : {OUT_DIR}")
    print(f"  配置文件   : {yaml_path}")
    print(f"\n下一步训练：")
    print(f"  python scripts/train_dms4class.py")
    print("=" * 60)


if __name__ == "__main__":
    main()
